    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        String username = userTxt.getText();
        String password = passTxt.getText();
        
        try {
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/school_system","root","");
            String query = "SELECT * FROM admin_login WHERE username = ? AND password = ?";
            PreparedStatement ps = (PreparedStatement) con.prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()) {
                dispose();
                view_course_reg vcr = new view_course_reg();
                vcr.show();
            } else {
                JOptionPane.showMessageDialog(null, "Wrong Username or Password", "TRY AGAIN", JOptionPane.WARNING_MESSAGE);
                userTxt.setText("");
                passTxt.setText("");
            }
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "An Error Occurred", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginBtnActionPerformed

